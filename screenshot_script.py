# Set up headless Chrome
chrome_options = Options()
chrome_options.add_argument('--headless')
chrome_options.add_argument('--disable-gpu')
chrome_options.add_argument('--window-size=1280x1024')
chrome_driver_path = '/Users/YourAddress/Desktop/chromedriver'

# Create output directory for screenshots
output_dir = 'screenshotsUSA'
if not os.path.exists(output_dir):
    os.makedirs(output_dir)

# Visit each website and take a screenshot of the home page
for index, website_name in enumerate(websites):
    try:
        # Visit the website
        driver = webdriver.Chrome(chrome_driver_path, options=chrome_options)
        driver.get(f'https://www.{website_name.lower()}.com')

        # Give time for the page to load
        time.sleep(5)

        # Take a screenshot of the entire webpage
        screenshot_path = os.path.join(output_dir, f'{website_name}.png')
        driver.save_screenshot(screenshot_path)
        print(f'Screenshot saved for {website_name} at {screenshot_path}')
    except Exception as e:
        print(f'Error capturing screenshot for {website_name}: {str(e)}')
        notCaptured.append(website_name)
    finally:
        driver.quit()

print(f'{notCaptured}')
print('Process completed.')
